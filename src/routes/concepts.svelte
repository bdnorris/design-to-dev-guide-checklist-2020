<h2 id="responsive-design">Responsive Design</h2>

<blockquote>
<p><em>All materials, whether wood or pixels, have a grain, and that grain suggests the best way to work. Go with the grain and one will find sturdiness combined with tremendous flexibility‚Äâ‚Äî‚Äâa natural and exciting give that grounds decisions and excites with possibilities. Work against the grain and the work becomes precarious, difficult and fragile. Instead of the elegant bending that software requires to adjust to different screens, uses, and situations, the work breaks because it cannot adapt.</em></p>
</blockquote>

<p>&mdash;<strong>Frank Chimero</strong> from the forward of <em>Learn to Code Now!</em> by the <strong>SuperHi Team</strong></p>

<p>There are a multitude of devices and screen sizes in use today, and most of them are capable of accessing the web. Designing sites that can run anywhere is a significant challenge. Responsive Web Design (RWD) is an approach to this problem.</p>

<p>Designing for the multitude of canvas dimensions is one of the most significant differences between print and digital design. We are now ten years into responsive design as an industry standard design approach, yet, the current state of design tools are not quite up to the task of assisting us in this approach (although, they are getting better).</p>

<p>So until our design software forces us to think this way, it&#39;s up to you to make sure you fill that gap. Thinking of your design canvas as a flexible document rather than one with defined boundaries is the first step.</p>

<h3 id="responsive-behavior">Responsive Behavior</h3>

<p>There are several different ways your content can behave as the viewport size changes. Item&#39;s dimensions can flex, they can reflow (e.g. stack), and otherwise change at a certain width, etc. Generally, we use &quot;responsive&quot; to mean all of these things, but it‚Äôs worth being familiar with the differences. It‚Äôs easier to show than describe:</p>

<ul>
	<li><a href="http://g-mops.net/epica_saitama/epica_layout/index_adaptive.html">Liquid vs Adaptive</a></li>
	<li><a href="https://css-tricks.com/the-difference-between-responsive-and-adaptive-design/">The Difference Between Responsive and Adaptive Design</a></li>
</ul>

<p>Most of the time, it‚Äôs obvious from the design files which approach certain elements of the page should take, but not always. That‚Äôs why it‚Äôs good to have a common language with your front-end developer to help describe responsive behaviors.</p>

<h3 id="breakpoints">Breakpoints</h3>

<p>Breakpoints are the pixel value dimensions of the browser‚Äôs viewport (browser window) that will trigger different visual layouts. They are typically based on the viewport width (since overflowing content can normally be reached by scrolling) but can be written for height as well. Major breakpoints should be chosen at the beginning of the design process.</p>

